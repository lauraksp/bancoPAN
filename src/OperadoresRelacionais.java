public class OperadoresRelacionais {

    public static void main (String[] args) {
        int numero1 = 3;
        int numero2 = 6;

        if (numero1 > numero2)
            System.out.println("Numero 1 maior que numero 2");
        if (numero1 < numero2)
            System.out.println("Numero 1 menor que numero 2");
    }
}
